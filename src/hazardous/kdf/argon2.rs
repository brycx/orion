/// The Argon2 version.
pub const ARGON2_VERSION: u32 = 0x13;

/// The Argon2 variant (i).
pub const ARGON2_VARIANT: u32 = 1;

#[must_use]
#[inline(always)]
///
const fn lower_mult_add(x: u64, y: u64) -> u64 {
	let mask = 0xFFFF_FFFFu64;
	let x_l = x & mask;
	let y_l = y & mask;
	let xy = x_l.wrapping_mul(y_l);
	x.wrapping_add(y.wrapping_add(xy.wrapping_add(xy)))
}

///
fn g(a: &mut u64, b: &mut u64, c: &mut u64, d: &mut u64) {
	*a = lower_mult_add(*a, *b);
	*d = (*d ^ *a).rotate_right(32);
	*c = lower_mult_add(*c, *d);
	*b = (*b ^ *c).rotate_right(24);
	*a = lower_mult_add(*a, *b);
	*d = (*d ^ *a).rotate_right(16);
	*c = lower_mult_add(*c, *d);
	*b = (*b ^ *c).rotate_right(63);
}

///
fn permutation_p(bytes: &mut [u64]) {
	debug_assert!(bytes.len() == 16);

	let mut v0 = bytes[0];
	let mut v1 = bytes[1];
	let mut v2 = bytes[2];
	let mut v3 = bytes[3];
	let mut v4 = bytes[4];
	let mut v5 = bytes[5];
	let mut v6 = bytes[6];
	let mut v7 = bytes[7];
	let mut v8 = bytes[8];
	let mut v9 = bytes[9];
	let mut v10 = bytes[10];
	let mut v11 = bytes[11];
	let mut v12 = bytes[12];
	let mut v13 = bytes[13];
	let mut v14 = bytes[14];
	let mut v15 = bytes[15];

	g(&mut v0, &mut v4, &mut v8, &mut v12);
	g(&mut v1, &mut v5, &mut v9, &mut v13);
	g(&mut v2, &mut v6, &mut v10, &mut v14);
	g(&mut v3, &mut v7, &mut v11, &mut v15);
	g(&mut v0, &mut v5, &mut v10, &mut v15);
	g(&mut v1, &mut v6, &mut v11, &mut v12);
	g(&mut v2, &mut v7, &mut v8, &mut v13);
	g(&mut v3, &mut v4, &mut v9, &mut v14);

	bytes[0] = v0;
	bytes[1] = v1;
	bytes[2] = v2;
	bytes[3] = v3;
	bytes[4] = v4;
	bytes[5] = v5;
	bytes[6] = v6;
	bytes[7] = v7;
	bytes[8] = v8;
	bytes[9] = v9;
	bytes[10] = v10;
	bytes[11] = v11;
	bytes[12] = v12;
	bytes[13] = v13;
	bytes[14] = v14;
	bytes[15] = v15;
}

/// H0 as defined in the specification.
fn h0(
	lanes: u32,
	hash_length: u32,
	memory_kib: u32,
	passes: u32,
	p: &[u8],
	s: &[u8],
	k: &[u8],
	x: &[u8],
) -> [u8; 72] {
	// We save additional 8 bytes in H0 for when the first two block are processed.
	// So that this may contain two little-endian integers.
	let mut h_0 = [0u8; 72];
	let mut hasher = Blake2b::new(None, BLAKE2B_OUTSIZE).unwrap();

	// Collect the first part to reduce times we update the hasher state.
	let mut first: [u8; 28] = [0u8; 28];
	first[0..4].copy_from_slice(&lanes.to_le_bytes());
	first[4..8].copy_from_slice(&hash_length.to_le_bytes());
	first[8..12].copy_from_slice(&memory_kib.to_le_bytes());
	first[12..16].copy_from_slice(&passes.to_le_bytes());
	first[16..20].copy_from_slice(&ARGON2_VERSION.to_le_bytes());
	first[20..24].copy_from_slice(&ARGON2_VARIANT.to_le_bytes());
	first[24..28].copy_from_slice(&(p.len() as u32).to_le_bytes());

	hasher.update(&first).unwrap();
	hasher.update(p).unwrap();
	hasher.update(&(s.len() as u32).to_le_bytes()).unwrap();
	hasher.update(s).unwrap();
	hasher.update(&(k.len() as u32).to_le_bytes()).unwrap();
	hasher.update(k).unwrap();
	hasher.update(&(x.len() as u32).to_le_bytes()).unwrap();
	hasher.update(x).unwrap();
	h_0[0..BLAKE2B_OUTSIZE].copy_from_slice(hasher.finalize().unwrap().as_ref());

	h_0
}

///
pub struct Argon2i {
	working_block: [u64; 128],
	passes: u32,
	lanes: u32,
	kib: u32,
	lane_len: u32,
}

impl Argon2i {
	///
	pub fn new(passes: u32, kib: u32) -> Self {
		let lanes = 1; // 0x00ffffff < lanes && lanes > 1 == TRUE
		if passes < 1 {
			panic!();
		}
		if (kib as u64) < 8 * lanes as u64 {
			panic!();
		}
		let lane_len = kib / (4 * lanes) * 4;

		Self {
			working_block: [0u64; 128],
			passes,
			lanes,
			kib,
			lane_len,
		}
	}

	#[cfg(test)]
	///
	pub fn new_test() -> Self {
		let lanes = 1;
		let lane_len = 8 / (4 * lanes) * 4;

		Self {
			working_block: [0u64; 128],
			passes: 0,
			lanes: 1,
			kib: 8,
			lane_len,
		}
	}

	///
	fn fill_block(&mut self) {
		// Columns.
		for col in self.working_block.chunks_mut(16) {
			permutation_p(col);
		}

		// Rows.
		let mut idx = 0;
		let mut tmp = [0u64; 16];
		while idx < 16 {
			tmp[0] = self.working_block[idx];
			tmp[1] = self.working_block[idx + 1];
			tmp[2] = self.working_block[idx + 16];
			tmp[3] = self.working_block[idx + 17];
			tmp[4] = self.working_block[idx + 32];
			tmp[5] = self.working_block[idx + 33];
			tmp[6] = self.working_block[idx + 48];
			tmp[7] = self.working_block[idx + 49];
			tmp[8] = self.working_block[idx + 64];
			tmp[9] = self.working_block[idx + 65];
			tmp[10] = self.working_block[idx + 80];
			tmp[11] = self.working_block[idx + 81];
			tmp[12] = self.working_block[idx + 96];
			tmp[13] = self.working_block[idx + 97];
			tmp[14] = self.working_block[idx + 112];
			tmp[15] = self.working_block[idx + 113];

			permutation_p(&mut tmp);

			self.working_block[idx] = tmp[0];
			self.working_block[idx + 1] = tmp[1];
			self.working_block[idx + 16] = tmp[2];
			self.working_block[idx + 17] = tmp[3];
			self.working_block[idx + 32] = tmp[4];
			self.working_block[idx + 33] = tmp[5];
			self.working_block[idx + 48] = tmp[6];
			self.working_block[idx + 49] = tmp[7];
			self.working_block[idx + 64] = tmp[8];
			self.working_block[idx + 65] = tmp[9];
			self.working_block[idx + 80] = tmp[10];
			self.working_block[idx + 81] = tmp[11];
			self.working_block[idx + 96] = tmp[12];
			self.working_block[idx + 97] = tmp[13];
			self.working_block[idx + 112] = tmp[14];
			self.working_block[idx + 113] = tmp[15];

			idx += 2;
		}
	}

	///
	pub fn g_xor(&mut self, block_x: &[u64], block_y: &[u64], out: &mut [u64]) {
		// R = X ^ Y
		for idx in 0..128 {
			self.working_block[idx] = block_x[idx] ^ block_y[idx];
			out[idx] ^= self.working_block[idx];
		}

		self.fill_block();

		// `out` = Z ^ R
		for (el_out, el_z) in out.iter_mut().zip(self.working_block.iter()) {
			*el_out ^= el_z;
		}
	}

	///
	pub fn g_two(&mut self, block: &[u64], out: &mut [u64]) {
		self.working_block.copy_from_slice(block);
		self.fill_block();

		for (el_out, &el_src) in self.working_block.iter_mut().zip(block.iter()) {
			*el_out ^= el_src;
		}

		let mut tmp_block = [0u64; 128];
		tmp_block.copy_from_slice(&self.working_block);

		self.fill_block();

		// `out` = Z ^ R
		for (el_out, (el_z, el_tmp)) in out
			.iter_mut()
			.zip(self.working_block.iter().zip(tmp_block.iter()))
		{
			*el_out = el_z ^ el_tmp;
		}
	}
}

